package Aula17;

public class loopFor {

	public static void main(String[] args) {
		
		for (int i=0; i < 5 ; i++) {
			System.out.println("i tem valor: " + i);
		}
		
		//fora do for a variavel int não existe.
		
		for (int i=5; i > 0 ; i--) {
			System.out.println("i tem valor: " + i);
		}

	}

}
